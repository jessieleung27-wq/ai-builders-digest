#!/usr/bin/env node

// ============================================================================
// Run Daily Digest
// ============================================================================
// Complete workflow: fetch data → generate digest → save → push to GitHub
// Usage: node scripts/run-daily-digest.js
// ============================================================================

import { execSync } from 'child_process';
import { writeFile, mkdir } from 'fs/promises';
import { join } from 'path';
import { homedir } from 'os';

const SKILL_DIR = '/root/skills/follow-builders';
const USER_DIR = join(homedir(), '.follow-builders');
const TODAY = new Date().toISOString().split('T')[0];

async function main() {
  console.log(`\n🦐 AI Builders Digest - ${TODAY}\n`);

  // Step 1: 准备数据
  console.log('📡 Fetching data...');
  const dataOutput = execSync(`cd ${SKILL_DIR}/scripts && node prepare-digest.js`, {
    encoding: 'utf-8'
  });

  const data = JSON.parse(dataOutput);
  console.log(`   ✓ ${data.stats.xBuilders} builders, ${data.stats.podcastEpisodes} podcast episodes`);

  // Step 2: 保存统计数据
  await mkdir(USER_DIR, { recursive: true });
  await writeFile(
    join(USER_DIR, 'digest-stats.json'),
    JSON.stringify(data.stats, null, 2),
    'utf-8'
  );

  // Step 3: 这里应该有 LLM 生成摘要的部分
  // 但在 cron 任务中，这会由 agent 完成
  // 我们假设 digest 文件会在 LLM 生成后被保存

  console.log('\n✅ Data preparation complete!');
  console.log('📝 The digest will be generated by the agent and pushed to GitHub.\n');
}

main().catch(err => {
  console.error('❌ Error:', err.message);
  process.exit(1);
});
